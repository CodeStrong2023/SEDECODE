# sistema decimal
a = 10
print(a)

# sistema binario
a = 0b1010
print(a)

# sistema octal
a = 0o12
print(a)

# sistema hexadecimal
a = 0xa
print(a)

# --------------------------------------------------------------------------------------------------------------

# base decimal
a = int('23', 10)
print(a)

# base binario
a = int('1010', 2)
print(a)

# base octal
a = int('12', 8)
print(a)

# base hexadecimal
a = int('A', 16)
print(a)

