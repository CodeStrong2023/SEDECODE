# sistema decimal
num_decimal = 10
print(num_decimal)

# sistema binario
num_binario = 0b1010
print(num_binario)

# sistema octal
num_octal = 0o12
print(num_octal)

# sistema hexadecimal
num_hexadecimal = 0xa
print(num_hexadecimal)

# --------------------------------------------------------------------------------------------------------------

# base decimal
num_base_decimal = int('23', 10)
print(num_base_decimal)

# base binario
num_base_binario = int('1010', 2)
print(num_base_binario)

# base octal
num_base_octal = int('12', 8)
print(num_base_octal)

# base hexadecimal
num_base_hexadecimal = int('A', 16)
print(num_base_hexadecimal)

import math

# Infinito positivo
infinito_positivo = float('inf')
print(infinito_positivo)
print(math.isinf(infinito_positivo))

# Infinito negativo
infinito_negativo = float('-inf')
print(infinito_negativo)
print(math.isinf(infinito_negativo))
